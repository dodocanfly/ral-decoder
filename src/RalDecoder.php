    public static function toHex(int $ralNumber): string
    {
        return self::HEX[$ralNumber][0];
    }


    public static function toRgb(int $ralNumber): array
    {
        return [
            hexdec(substr(self::toHex($ralNumber), 0, 2)),
            hexdec(substr(self::toHex($ralNumber), 2, 2)),
            hexdec(substr(self::toHex($ralNumber), 4, 2)),
        ];
    }


    public static function toRgbStr(int $ralNumber): string
    {
        return implode(',', self::toRgb($ralNumber));
    }


    public static function toCssRgbStr(int $ralNumber): string
    {
        return 'rgb(' . self::toRgbStr($ralNumber) . ')';
    }


    public static function isDark(int $ralNumber): bool
    {
        return self::HEX[$ralNumber][1];
    }
